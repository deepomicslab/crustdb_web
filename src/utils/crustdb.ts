export const DiseaseStageColor = (style: any) => {
    if (style === 'Normal') {
        return 'success'
    }
    if (
        style === 'AJCC pathologic stage pT2 pN1a pMX, ER - /HER2 + /PR-' ||
        style === 'TNM stage T2N1M0, ER+ /HER2+ /PR-'
    ) {
        return 'info'
    }
    if (
        style === 'Non-Small Cell Lung Cancer IIB' ||
        style === 'Non-Small Cell Lung Cancer IIIA' ||
        style === 'Hepatocellular Carcinoma'
    ) {
        return 'warning'
    }
    return 'error'
}
